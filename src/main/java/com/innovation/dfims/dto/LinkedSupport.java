package com.innovation.dfims.dto;

public class LinkedSupport {
	private String supportName;
	private String supportMobile;
	private String supportEmail;
	private String supportAddress;
	private String supportLat;
	private String supportLong;
	private String actualMobile;

	public String getActualMobile() {
		return actualMobile;
	}

	public void setActualMobile(String actualMobile) {
		this.actualMobile = actualMobile;
	}

	public String getSupportEmail() {
		return supportEmail;
	}

	public void setSupportEmail(String supportEmail) {
		this.supportEmail = supportEmail;
	}

	public String getSupportName() {
		return supportName;
	}

	public void setSupportName(String supportName) {
		this.supportName = supportName;
	}

	public String getSupportMobile() {
		return supportMobile;
	}

	public void setSupportMobile(String supportMobile) {
		this.supportMobile = supportMobile;
	}

	public String getSupportAddress() {
		return supportAddress;
	}

	public void setSupportAddress(String supportAddress) {
		this.supportAddress = supportAddress;
	}

	public String getSupportLat() {
		return supportLat;
	}

	public void setSupportLat(String supportLat) {
		this.supportLat = supportLat;
	}

	public String getSupportLong() {
		return supportLong;
	}

	public void setSupportLong(String supportLong) {
		this.supportLong = supportLong;
	}

}
