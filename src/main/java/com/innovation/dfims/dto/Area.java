package com.innovation.dfims.dto;

public class Area {
	private int areaId;
	private String areaName;
	private String areaZipcode;
	private String areaMinCitizen;
	private String areaMaxCitizen;
	public int getAreaId() {
		return areaId;
	}
	public void setAreaId(int areaId) {
		this.areaId = areaId;
	}
	public String getAreaName() {
		return areaName;
	}
	public void setAreaName(String areaName) {
		this.areaName = areaName;
	}
	public String getAreaZipcode() {
		return areaZipcode;
	}
	public void setAreaZipcode(String areaZipcode) {
		this.areaZipcode = areaZipcode;
	}
	public String getAreaMinCitizen() {
		return areaMinCitizen;
	}
	public void setAreaMinCitizen(String areaMinCitizen) {
		this.areaMinCitizen = areaMinCitizen;
	}
	public String getAreaMaxCitizen() {
		return areaMaxCitizen;
	}
	public void setAreaMaxCitizen(String areaMaxCitizen) {
		this.areaMaxCitizen = areaMaxCitizen;
	}
}
