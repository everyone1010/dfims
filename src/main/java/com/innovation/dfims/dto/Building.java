package com.innovation.dfims.dto;

public class Building {
	private int buildingId;
	private String buildingName;
	private String buildingAddress;
	private String buildingPocName;
	private String buildingPocMobile;
	private String buildingPocEmail;
	private String buildingCitizenCount;
	private int areaId;
	private String buildingLat;
	private String buildingLong;

	public int getBuildingId() {
		return buildingId;
	}
	public void setBuildingId(int buildingId) {
		this.buildingId = buildingId;
	}
	public String getBuildingName() {
		return buildingName;
	}
	public void setBuildingName(String buildingName) {
		this.buildingName = buildingName;
	}
	public String getBuildingAddress() {
		return buildingAddress;
	}
	public void setBuildingAddress(String buildingAddress) {
		this.buildingAddress = buildingAddress;
	}
	public String getBuildingPocName() {
		return buildingPocName;
	}
	public void setBuildingPocName(String buildingPocName) {
		this.buildingPocName = buildingPocName;
	}
	public String getBuildingPocMobile() {
		return buildingPocMobile;
	}
	public void setBuildingPocMobile(String buildingPocMobile) {
		this.buildingPocMobile = buildingPocMobile;
	}
	public String getBuildingPocEmail() {
		return buildingPocEmail;
	}
	public void setBuildingPocEmail(String buildingPocEmail) {
		this.buildingPocEmail = buildingPocEmail;
	}
	public String getBuildingCitizenCount() {
		return buildingCitizenCount;
	}
	public void setBuildingCitizenCount(String buildingCitizenCount) {
		this.buildingCitizenCount = buildingCitizenCount;
	}
	public int getAreaId() {
		return areaId;
	}
	public void setAreaId(int areaId) {
		this.areaId = areaId;
	}
	public String getBuildingLat() {
		return buildingLat;
	}
	public void setBuildingLat(String buildingLat) {
		this.buildingLat = buildingLat;
	}
	public String getBuildingLong() {
		return buildingLong;
	}
	public void setBuildingLong(String buildingLong) {
		this.buildingLong = buildingLong;
	}

}
